package com.javarush.task.task04.task0438;

/* 
Рисуем линии
*/

public class Solution {
    public static void main(String[] args) throws Exception {
        for (int i = 1; i <= 10; i = i + 1) {

            if (i == 1) {
                for (int j = 1; j <= 10; j = j + 1) {
                    System.out.print("8");
                }

            }


            System.out.println("8");
        }


    }
}

